// Add some interactive features
document.addEventListener('DOMContentLoaded', () => {
    // Add hover effects to research items
    const researchItems = document.querySelectorAll('.research-item');
    researchItems.forEach(item => {
        item.addEventListener('mouseenter', () => {
            item.style.transform = 'translateY(-10px) scale(1.02)';
        });

        item.addEventListener('mouseleave', () => {
            item.style.transform = 'translateY(0) scale(1)';
        });
    });

    // Add click to copy email functionality
    const emailElement = document.querySelector('.contact-item p');
    if (emailElement && emailElement.textContent.includes('@')) {
        emailElement.style.cursor = 'pointer';
        emailElement.addEventListener('click', () => {
            navigator.clipboard.writeText(emailElement.textContent).then(() => {
                // Show a temporary "Copied!" message
                const originalText = emailElement.textContent;
                emailElement.textContent = 'Copied!';
                emailElement.style.color = '#27ae60';
                setTimeout(() => {
                    emailElement.textContent = originalText;
                    emailElement.style.color = '#666';
                }, 2000);
            });
        });
    }
}); 